class User {
  int userId;
  String username;
  String firstName;
  String lastName;
  String email;
  String phoneNo;
  String country;
  String city;
  String birthday;
  int accessLevel;

  User({
    required this.userId,
    required this.username,
    required this.firstName,
    required this.lastName,
    required this.email,
    required this.phoneNo,
    required this.country,
    required this.city,
    required this.birthday,
    required this.accessLevel,
  });
}
